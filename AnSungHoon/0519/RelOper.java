//관계연산자

import java.util.Scanner;

public class RelOper {
	public static void main(String[] args) {
		System.out.println(55 == 55); // 같은가?
		System.out.println(55 != 55); // 다른가? : 부정형
		System.out.println(55 > 55);
		System.out.println(55 < 55);
		System.out.println(55 >= 55);
		System.out.println(55 <= 55);
	}
}